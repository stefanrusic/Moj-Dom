function boja(x)
{
var obj=document.getElementById("tabela2");
switch(x)
{
case 1:
{
obj.style.background="grey";
obj.style.color="yellow";
}
break;
case 2:
{
obj.style.background="lightgreen";
obj.style.color="black";
}
break;
case 3:
{
obj.style.background="gold";
obj.style.color="red";
}
break;
}
return;
}
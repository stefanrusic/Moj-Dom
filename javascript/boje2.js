function boja(x)
{
var obj=document.getElementById("tabela3");
switch(x)
{
case 1:
{
obj.style.background="purple";
obj.style.color="yellow";
}
break;
case 2:
{
obj.style.background="orange";
obj.style.color="yellow";
}
break;
case 3:
{
obj.style.background="brown";
obj.style.color="black";
}
break;
}
return;
}